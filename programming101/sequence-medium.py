#exercise 1 - Given two lists of numbers of the same length, 
# create a new list by multiplying the pairs of numbers in corresponding positions in the two lists.

list_1 = [10,5,3]
list_2 = [10,4,5]
result = []

i = 0

while i < len(list_1):
    result.append(list_1[i] * list_2[i])
    i += 1

print(result)

